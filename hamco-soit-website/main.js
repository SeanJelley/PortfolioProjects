function picture_rotation() {
    if (window.screen.width <= 425) {
        pics = ["labs/datacenter.jpg", "labs/n222.jpg", "labs/n241.jpg", "labs/n243.jpg"];
        random_img = Math.floor(Math.random() * pics.length);
        document.getElementById("parallax").style.backgroundImage = "url(" + pics[random_img] + ")";
        document.getElementById("logoSize").style.width = "8rem"
    } else {
        pics = ["labs/datacenter.jpg", "labs/ithall.jpg", "labs/n222.jpg", "labs/n241.jpg", "labs/n243.jpg"];
        random_img = Math.floor(Math.random() * pics.length);
        document.getElementById("parallax").style.backgroundImage = "url(" + pics[random_img] + ")"; 
        document.getElementById("logoSize").style.width = "15rem"
    }
    
}